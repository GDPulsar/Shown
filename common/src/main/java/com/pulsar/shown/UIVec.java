package com.pulsar.shown;

public class UIVec {
    public double scaleX;
    public double scaleY;
    public int offsetX;
    public int offsetY;

    public UIVec(double scaleX, double scaleY, int offsetX, int offsetY) {
        this.scaleX = scaleX;
        this.scaleY = scaleY;
        this.offsetX = offsetX;
        this.offsetY = offsetY;
    }
}
